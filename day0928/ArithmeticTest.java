class ArithmeticTest 
{
	public static void main(String[] args) 
	{
		int a = 5;
		int b = 2;
		int add = a + b;
		int sub = a - b;
		int multi = a * b;
		int div = a / b;
		int mode = a % b;
		
		System.out.println(a + "+" + b + "=" + add);
		System.out.println(a + "-" + b + "=" + sub);
		System.out.println(a + "*" + b + "=" + multi);
		System.out.println(a + "/" + b + "=" + div);
		System.out.println(a + "%" + b + "=" + mode);

	}
}
